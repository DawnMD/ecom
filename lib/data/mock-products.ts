/** Deterministic pick based on seed so mock data is stable */
function pick<T>(arr: readonly T[], count: number, seed = 0): T[] {
  const indices = arr
    .map((_, i) => (i * 7 + seed) % arr.length)
    .filter((v, i, a) => a.indexOf(v) === i)
    .slice(0, count);
  return indices.map((idx) => arr[idx]);
}

function pickSizes(seed = 0): string[] {
  const n = 3 + (seed % 4);
  return pick([...SIZES], n, seed);
}

